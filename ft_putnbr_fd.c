#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int nb;

	nb = n;
	if (n < 0)
	{
		putchar('-');
		nb *= -1;
	}
	if (nb < 10)
	{
		putchar(nb + '0');
	}
	
}